                </span></p>
            <p>Le Dark Vlad possède trois sorts. Les trois sorts sont présentés un peu plus bas. Pour vaincre le Dark Vlad le but est d'<span id="fontbold">éviter de se faire toucher</span> par les sorts <span id="fontbold">Flammes Diagonales</span> et <span id="fontbold">Épée Kibrûl</span> car ils tapent fort et en plus vous mettent -50% de résistance infinie et a partir de ce moment le combat devient beacoup plus compliqué car il vous frappera beaucoup plus fort.</p>
            <p>Pour éviter ces sorts il va falloir réfléchir un peu sur votre placement et à quel moment utiliser vos sorts <span id="fontbold">Courage</span> ou <span id="fontbold">Vivacité</span> :
            </p>
            <ul>
                <li id="lispace">Regardez les boosts du monstre, dans lesquels seront affichés les relances de chacun des sorts à éviter. Par exemple vous pourrez voir "Flammes Diagonales dans x tours".</li>
                <li id="lispace">Pour éviter des perde des résistances le meilleur moyen est de rester en ligne et à distance du DV. En restant en ligne il ne pourra pas venir dans votre diagonale vous frapper (à moins que vous ne soyez à moins de 2PO de lui ou qu'il se boost en PM). Vous pouvez aussi éviter d'être frappé par le sort Flammes Diagonales grâce aux obstacles car ce sort à besoin d'une ligne de vue.
                </li>
                <li id="lispace">Arrangez vous pour être le plus souvent en ligne avec DV et au minimum à 6PO en ligne de lui. Attention au sort Vitesse qui le boost de temps en temps de 3PM (vous pouvez voir la relance dans ses boosts).
                </li>
                <li id="lispace">Quand vous arrivez en bord de map arrangez vous pour repartir dans l'autre sens en changeant de place avec lui et en remontant tout en vous boostant éventuellement en PM.
                </li>
                <li id="lispace">Finalement n'oubliez pas de le taper de temps en temps.
                </li>
            </ul>
